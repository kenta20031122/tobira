async function main() {
  const normalized = spots.map((s) => ({ website_url: '', ...s }));

  console.log(`Inserting ${normalized.length} extra Chubu spots…`);

  const { error } = await supabase
    .from('spots')
    .upsert(normalized, { onConflict: 'id' });

  if (error) {
    console.error('Error:', error.message);
    process.exit(1);
  }

  console.log('Done! All extra Chubu spots inserted successfully.');

  // Print summary by prefecture
  const counts = {};
  for (const s of normalized) {
    counts[s.prefecture] = (counts[s.prefecture] ?? 0) + 1;
  }
  console.table(counts);
}

main();
