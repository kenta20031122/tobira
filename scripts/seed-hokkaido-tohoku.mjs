async function seed() {
  console.log(`\n📍 Seeding ${spots.length} spots for Hokkaido & Tohoku...\n`);

  let inserted = 0;
  let errors = 0;

  for (const spot of spots) {
    const { error } = await supabase
      .from('spots')
      .upsert(spot, { onConflict: 'id' });

    if (error) {
      console.error(`❌ ${spot.id}: ${error.message}`);
      errors++;
    } else {
      const flag = spot.is_premium ? '⭐' : '✅';
      console.log(`${flag} ${spot.id.padEnd(35)} ${spot.prefecture}`);
      inserted++;
    }
  }

  console.log(`\n✅ Done: ${inserted} inserted, ${errors} errors\n`);
}

