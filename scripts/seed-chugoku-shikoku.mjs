async function main() {
  console.log(`\n📁 Inserting ${spots.length} spots for 8 new prefectures...\n`);
  let success = 0;
  let errors = 0;

  for (const spot of spots) {
    if (!spot.website_url) spot.website_url = '';
    const { error } = await supabase.from('spots').upsert(spot, { onConflict: 'id' });
    if (error) {
      console.error(`❌ ${spot.id.padEnd(35)} ${error.message}`);
      errors++;
    } else {
      console.log(`✅ ${spot.id.padEnd(35)} ${spot.prefecture}`);
      success++;
    }
  }

  console.log(`\n✅ Done: ${success} inserted, ${errors} errors\n`);
}

main().catch(console.error);
